// Now what is the different between var and let?
// 1. var is function scoped, let is block scoped
// 2. var can be re-declared, let cannot be re-declared
// 3. var is hoisted, let is not hoisted


// Example 
let scoring = 102 //Number datatype
let name =  "Ben" //String datatype
let isLoggedIn = false  //Boolean datatype 


//object
